import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { composeLocalBackendOrigin, resolveLanHost } from './lan-host.mjs';

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const LOCAL_BACKEND_PORT = 3000;

export const VARIANTS = {
  local: {
    outputPath: resolve(REPO_ROOT, 'src/environments/environment.ios.local.ts'),
    production: false,
  },
  prod: {
    outputPath: resolve(REPO_ROOT, 'src/environments/environment.ios.prod.ts'),
    production: true,
  },
};

/**
 * Loads .env into process.env if present. A missing .env is fine — most
 * local dev needs no overrides at all, since the LAN host auto-detects.
 */
export function loadDotEnv(envPath = resolve(REPO_ROOT, '.env')) {
  try {
    process.loadEnvFile(envPath);
  } catch (error) {
    if (error && typeof error === 'object' && 'code' in error && error.code === 'ENOENT') {
      return;
    }

    throw error;
  }
}

export function resolveLocalBackendOrigin(envValues = {}, options = {}) {
  const explicitOrigin = envValues.IOS_BACKEND_ORIGIN_LOCAL?.trim();
  if (explicitOrigin) {
    return explicitOrigin;
  }

  const lanHost = resolveLanHost(envValues, options);
  const composed = composeLocalBackendOrigin(lanHost, LOCAL_BACKEND_PORT);

  if (!composed) {
    throw new Error(
      'Unable to auto-detect a LAN IP for the iOS local build. Set IOS_LAN_HOST (or IOS_BACKEND_ORIGIN_LOCAL) in .env — see .env.example.'
    );
  }

  return composed;
}

export function resolveProdBackendOrigin(envValues = {}) {
  const explicitOrigin = (envValues.IOS_BACKEND_ORIGIN_PROD ?? envValues.BACKEND_ORIGIN)?.trim();

  if (!explicitOrigin) {
    throw new Error(
      'IOS_BACKEND_ORIGIN_PROD (or BACKEND_ORIGIN) is required to build the ios-prod environment — no hosted backend exists yet (see docs/MIGRATION-CHECKLIST.md Phase 10). Set it in .env once one does — see .env.example.'
    );
  }

  return explicitOrigin;
}

export function buildEnvironmentIosSource({ production, apiUrl }) {
  return `// Generated by scripts/write-environment-ios.mjs — do not edit.
// Re-run npm run build:ios:local (or build:ios:prod) after changing backend origin env vars.

export const environment = {
  production: ${String(production)},
  apiUrl: '${apiUrl}',
};
`;
}

export function generateEnvironmentIos({
  variant,
  envValues = process.env,
  options = {},
  writeFileSyncFn = writeFileSync,
  variants = VARIANTS,
} = {}) {
  const config = variants[variant];

  if (!config) {
    throw new Error(`Unknown iOS environment variant: ${String(variant)}`);
  }

  const apiUrl =
    variant === 'local'
      ? resolveLocalBackendOrigin(envValues, options)
      : resolveProdBackendOrigin(envValues);

  const source = buildEnvironmentIosSource({ production: config.production, apiUrl });
  writeFileSyncFn(config.outputPath, source, 'utf8');

  return { outputPath: config.outputPath, apiUrl, source };
}

function main() {
  const variant = process.argv[2];

  if (variant !== 'local' && variant !== 'prod') {
    console.error(
      '[write-environment-ios] Usage: node scripts/write-environment-ios.mjs <local|prod>'
    );
    process.exit(1);
  }

  loadDotEnv();

  try {
    const result = generateEnvironmentIos({ variant });
    console.log(
      `[write-environment-ios] Wrote ${result.outputPath.replace(`${REPO_ROOT}/`, '')} (apiUrl: ${result.apiUrl})`
    );
  } catch (error) {
    console.error(
      `[write-environment-ios] ${error instanceof Error ? error.message : String(error)}`
    );
    process.exit(1);
  }
}

const entrypoint = process.argv[1];
if (entrypoint && import.meta.url === pathToFileURL(resolve(entrypoint)).href) {
  main();
}
